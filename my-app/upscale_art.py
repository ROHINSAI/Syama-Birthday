
CAKE_ART = [
  "........................................",
  "...................^....................",
  "..................^|^...................",
  "..................|||...................",
  "..................|||...................",
  "..................|||...................",
  "..............@@@@@@@@@@@...............",
  "..............@@@@@@@@@@@...............",
  "..............@@@@@@@@@@@...............",
  "............################............",
  "............################............",
  "............################............",
  "............################............",
  "..........@@@@@@@@@@@@@@@@@@@@..........",
  "..........@@@@@@@@@@@@@@@@@@@@..........",
  "........########################........",
  "........########################........",
  "........########################........",
  "........########################........",
  "........########################........",
  "........########################........",
  "........................................", 
]

SCALE = 2

new_art = []
for row in CAKE_ART:
    new_row = ""
    for char in row:
        new_row += char * SCALE
    for _ in range(SCALE):
        new_art.append('"' + new_row + '",')

print("export const CAKE_ART = [")
for line in new_art:
    print(f"  {line}")
print("];")
